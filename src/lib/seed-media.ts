async function main() {
  // Clear existing media items first
  await prisma.mediaItem.deleteMany({})
  console.log("Cleared existing media items")

  const items = images.map((filename, idx) => ({
    title: `Làng Cao Phong ${idx + 1}`,
    description: null,
    type: "image" as const,
    url: `/${filename}`,
    thumbnail: null,
    sortOrder: idx,
    isActive: true,
  }))

  await prisma.mediaItem.createMany({ data: items })
  console.log(`✅ Seeded ${items.length} images into MediaItem`)
  await prisma.$disconnect()
}

main().catch(e => { console.error(e); process.exit(1) })
